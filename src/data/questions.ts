export function getQuestionsByLevel(levelId: number, count: number): Question[] {
  const levelQuestions = allQuestions.filter(q => q.levelId === levelId);
  const shuffled = [...levelQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

const questionsById = new Map(allQuestions.map((question) => [question.id, question]));

export function getQuestionsByIds(questionIds: number[]): Question[] {
  return questionIds
    .map((questionId) => questionsById.get(questionId))
    .filter((question): question is Question => Boolean(question));
}
